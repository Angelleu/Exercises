const mutants = ['Professor X','Cyclops','Angel','Beast','Phoenix'];
console.log(`Original List: ${mutants}`);

const sortedList = mutants.sort();
console.log(`Sorted List: ${sortedList}`);

const reversedList = mutants.reverse();
console.log(`Reversed List: ${reversedList}`);

const joinedList = mutants.join(' * ');
console.log(`Joined List: ${joinedList}`);
